export const getTechnologyKnowledge = (topic: string) => {
  const topicLower = topic.toLowerCase();
  
  if (topicLower.includes('gartner') || topicLower.includes('value framework')) {
    return TechnologyKnowledgeBase.aiInnovation.gartnerValueFramework;
  }
  
  if (topicLower.includes('agent') || topicLower.includes('multi-agent')) {
    return TechnologyKnowledgeBase.technologyTopics.multiAgentSystems;
  }
  
  if (topicLower.includes('edge') || topicLower.includes('local')) {
    return TechnologyKnowledgeBase.technologyTopics.edgeAI;
  }
  
  if (topicLower.includes('security') || topicLower.includes('compliance')) {
    return TechnologyKnowledgeBase.securityAndCompliance;
  }
  
  if (topicLower.includes('journey') || topicLower.includes('experimentation')) {
    return TechnologyKnowledgeBase.journeyUseCases.experimentationHubJourney;
  }
  
  return TechnologyKnowledgeBase;
};

export const getCaseStudies = (industry?: string) => {
  if (industry) {
    return TechnologyKnowledgeBase.caseStudies.aiImplementationSuccess.filter(
      cs => cs.industry.toLowerCase().includes(industry.toLowerCase())
    );
  }
  return TechnologyKnowledgeBase.caseStudies.aiImplementationSuccess;
};

