export const getTechnologyInfo = (topic: string) => {
  switch (topic.toLowerCase()) {
    case 'emotional':
      return TechnologyKnowledgeBase;
    case 'ai democratization':
    case 'democratization':
      return TechnologyKnowledgeBase.industryTrends.aiDemocratization;
    case 'no-code':
    case 'low-code':
    case 'nocode':
      return TechnologyKnowledgeBase.industryTrends.noCodeLowCode;
    case 'agentic':
    case 'agents':
    case 'agentic ai':
      return TechnologyKnowledgeBase.industryTrends.agenticPlatforms;
    case 'big tech':
    case 'market leaders':
      return TechnologyKnowledgeBase.marketLandscape.bigTechPlayers;
    case 'conferences':
    case 'tech conferences':
      return TechnologyKnowledgeBase.upcomingConferences.technology;
    case 'healthcare conferences':
      return TechnologyKnowledgeBase.upcomingConferences.healthcare;
    case 'digital health':
    case 'medical devices':
      return TechnologyKnowledgeBase.digitalHealthDevices;
    case 'consortiums':
      return TechnologyKnowledgeBase.upcomingConferences.consortiums;
    default:
      return TechnologyKnowledgeBase;
  }
};

